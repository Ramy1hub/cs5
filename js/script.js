let currentLang = 'en';

function renderTabs() {
  const tabs = document.getElementById('tabs');
  tabs.innerHTML = '';
  categories.forEach((cat, idx) => {
    const btn = document.createElement('div');
    btn.className = 'tab' + (idx === 0 ? ' active' : '');
    btn.innerText = cat.name[currentLang];
    btn.onclick = () => setActiveTab(cat.id);
    btn.dataset.target = cat.id;
    tabs.appendChild(btn);
  });
}

function renderContent() {
  const contents = document.getElementById('contents');
  contents.innerHTML = '';
  categories.forEach((cat, idx) => {
    const section = document.createElement('div');
    section.className = 'tab-content' + (idx === 0 ? ' active' : '');
    section.id = cat.id;
    cat.links.forEach(link => {
      const card = document.createElement('div');
      card.className = `card zoom card-${cat.id}`;
      const a = document.createElement('a');
      a.href = link.url;
      a.target = '_blank';
      a.innerText = link.name;
      card.appendChild(a);
      section.appendChild(card);
    });
    contents.appendChild(section);
  });
}

function setActiveTab(id) {
  document.querySelectorAll('.tab').forEach(tab => {
    tab.classList.toggle('active', tab.dataset.target === id);
  });
  document.querySelectorAll('.tab-content').forEach(content => {
    if (content.id === id) {
      content.classList.add('fade-in', 'slide-in');
      content.classList.add('active');
      setTimeout(() => {
        content.classList.remove('fade-in');
        content.classList.remove('slide-in');
      }, 400);
    } else {
      content.classList.remove('active');
    }
  });
}


function toggleLanguage() {
  currentLang = currentLang === 'en' ? 'ar' : 'en';
  document.getElementById('title').innerText = currentLang === 'en' ? 'Link Hub' : 'مركز الروابط';
  document.querySelector('.lang-btn').innerText = currentLang === 'en' ? 'العربية' : 'English';
  renderTabs();
  renderContent();
  }
  
  
// Initial render
renderTabs();
renderContent();

